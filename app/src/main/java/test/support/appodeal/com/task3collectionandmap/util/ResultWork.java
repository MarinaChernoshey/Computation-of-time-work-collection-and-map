package test.support.appodeal.com.task3collectionandmap.util;

public class ResultWork {
    private int index;
    private double result;

    public ResultWork(int index, double result) {
        this.index = index;
        this.result = result;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public double getResult() {
        return result;
    }

    public void setResult(double result) {
        this.result = result;
    }
}
